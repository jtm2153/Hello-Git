package com.jtm13;

public class HashStudent {
    private String name;
    private String age;

    public HashStudent(String name, String age) {
        this.name = name;
        this.age = age;
    }

    public HashStudent() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
